                <hgroup class="text-center">
                    <h1>Conheça os benefícios da Arborização</h1>
                </hgroup>

                <p>
                    Muitas vezes por falta de conhecimento, pensamos que as árvores só servem para nos dar sombra ou
                    frutos. Felizmente, os benefícios vão muito além! Confira abaixo as principais vantagens de ter
                    uma árvore plantada em seu quintal ou calçada:
                </p>

                <div class="container col-6 font-size">
                    <ul>
                        <li>Ornamentar as vias públicas, especialmente em época de floração;</li>
                        <li>Proporcionar sombra para os pedestres e veículos;</li>
                        <li>Amenizar a poluição sonora;</li>
                        <li>Reduzir o impacto da água de chuva e seu escorrimento superficial;</li>
                        <li>Servir de abrigo à avifauna local;</li>
                        <li>Reduzir a poluição do ar provocada principalmente pela queima de combustíveis dos
                            veículos automotores;</li>
                        <li>Proporcionar conforto térmico e bem-estar psicológico às pessoas;</li>
                        <li>Proteger o lençol freático;</li>
                        <li>Amenizar a força do vento;</li>
                        <li>Valorizar os imóveis ao seu redor;</li>
                        <li>Proporcionar aumento na umidade relativa do ar;</li>
                        <li>Reduzir os impactos do aquecimento global;</li>
                        <li>Fornecer matéria orgânica para os solos;</li>
                        <li>Reduzir a incidência solar;</li>
                        <li>Retenção de poeira pelas folhas e troncos das árvores.</li>
                    </ul>
                </div>

                <p>
                    Como se vê, as árvores proporcionam benefícios ecológicos, estéticos, sociais e psicológicos,
                    contribuem para o lazer, conforto e bem-estar da população por já fazerem parte de nossa vida
                    diária. Além disso, as áreas verdes nas proximidades de edifícios e residências, estabilizam sua
                    temperatura interna, possibilitando o menor uso de condicionadores de ar e, consequentemente,
                    reduzindo o consumo de energia elétrica.
                </p>

            </div>


