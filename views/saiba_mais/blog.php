                <hgroup class="text-center">
                    <h1>O Amapá – sua geografia e deficiências</h1>
                </hgroup>

                <p>

                    Localizado no extremo norte do Brasil, numa região de clima equatorial úmido e dividido pela
                    Linha
                    do Equador, o Estado do Amapá, um dos mais novos da Federação, é um dos locais mais quentes
                    devido a
                    sua posição geográfica. O calor é intenso mesmo nos períodos de maior incidência de chuvas e
                    isso
                    provoca mais transtornos à população urbana do que um clima mais tolerável! Numa rápida passagem
                    pela capital amapaense, ao sentir na pele os efeitos provocados pela exposição aos raios
                    solares, é
                    possível perceber que a cidade é relativamente pobre no quesito arborização. Pouquíssimos
                    parques e
                    árvores oferecem abrigo contra o calor, que leva as pessoas a buscar soluções paliativas como
                    guarda-chuvas para se protegerem dos raios solares.

                </p>

                <p>

                    Desta forma, a arborização urbana surge como solução adequada e viável, desde que realizada de
                    forma
                    satisfatória, com plantio das espécies corretas em áreas também corretas, seguindo um
                    planejamento
                    bem estruturado e respeitando as características da cidade. Acreditamos que a médio e longo
                    prazo os
                    efeitos serão positivos e comprovadamente benéficos. Não só o clima será de melhor qualidade
                    como a
                    paisagem tende a ser transformada, refletindo inclusive na qualidade do ar, que em futuras
                    décadas
                    pode vir a piorar devido aos poluentes emitidos pelos veículos. Sabemos que as árvores não
                    diminuem
                    temperaturas de forma drástica, não são milagrosas. Mas podem influenciar diretamente na
                    sensação
                    térmica, trazendo melhor qualidade de vida e bem-estar à população, criando uma barreira natural
                    aos
                    raios UV.

                </p>
            </div>


            <!-- CONTEUDO 04 -->
            <div id="Semam" class="column col-md-10 p-5 ">

                <hgroup class="text-center">
                    <h1>Funções e deveres da Secretaria de Meio Ambiente</h1>
                </hgroup>


                <p>
                    No Brasil, está a cargo das prefeituras implantar e gerencias os projetos de arborização. Cabe a
                    cada município distribuir os riscos e benefícios da urbanização, induzindo um desenvolvimento
                    mais
                    inclusivo e sustentável. Em nossa cidade, a SEMAM - Secretaria Municipal do Meio Ambiente de
                    Macapá,
                    é o departamento responsável por cumprir este papel. São suas missões: “Proporcionar, promover e
                    desenvolver as políticas de meio ambiente do município e normas e padrões para a sua proteção,
                    defender e controlar, bem como verificar o seu cumprimento, em articulação com os sistemas
                    estadual
                    e federal de meio ambiente; Promover o zoneamento ambiental do Município, como a aplicação do
                    geoprocessamento das informações decorrentes.”
                </p>

                <p>
                    Dentro da SEMAM, existe ainda um departamento que cuida em específico de Paisagismo e
                    Arborização,
                    responsável por: “(...) elaborar, coordenar e supervisionar a execução de programas, projetos e
                    atividades de paisagismo, administração do Horto Municipal e arborização, visando promover ações
                    destinadas à melhoria da qualidade ambiental do município (...), e emitir relatórios mensal e
                    trimensal, enviando ao Secretário” (art. 253 da Lei Orgânica).
                </p>

                <p>
                    Visitamos o Horto Municipal para conhecer mais sobre as funções e principais
                    dificuldades no
                    dia a dia da SEMAM, bem como realizar uma entrevista com funcionários do departamento de
                    arborização. Pudemos também verificar melhor a dinâmica de trabalho, avaliar o espaço e
                    entender como andam os projetos de arborização em implantação no município.
                </p>

                <p>
                    O local é bastante amplo, com diversos canteiros e mudas prontas, tanto de espécies
                    arbóreas
                    quanto plantas ornamentais. Este é um dos principais trabalhos realizados pela equipe da
                    secretaria, que posteriormente cede mudas aos cidadãos interessados. Há também um
                    orquidário, com mais de 380 espécies, sendo que metade são nativas da Amazônia. Este
                    fica ao
                    lado de uma área de ressaca, cuja humidade favorece o desenvolvimento das orquídeas. A
                    sede
                    desse departamento situa-se num galpão subdividido em outras salas onde funcionam chefia
                    e
                    administração. Ali são gerenciados os processos de solicitação de remoção de árvores,
                    catalogação de espécies e desenvolvimento de novos projetos de arborização.
                </p>

                <div class="ImagensSemam">
                    <img src="app/img/ORQUIDARIO_SEMAM.jpg" alt="Orquidário da Semam" title="Orquidário Semam">
                    <img src="app/img/SEMAM.jpg" alt="Mudas da Semam" title="Mudas Semam">
                </div>

                <p>
                    Por outro lado, constatou-se que a estrutura precisa urgente de mais funcionários,
                    veículos
                    e computadores, que facilitariam ainda mais a realização das tarefas diárias e
                    propiciariam
                    um trabalho mais efetivo de pesquisa de espécies. Inclusive, uma sugestão dada por um
                    dos
                    entrevistados foi a criação de um banco de dados que registrasse e servisse de
                    monitoramento
                    aos trabalhos realizados, de modo a terem prosseguimento efetivo por quem quer que
                    assumisse
                    a coordenação dos projetos, já que tal ferramenta inexiste. Há universidades e
                    faculdades
                    parceiras que já realizam trabalhos paralelos de pesquisa, de modo a suprir em partes
                    essa
                    carência da Secretaria. Somado a isto, o conhecimento acadêmico na área da programação
                    poderia somar no sentido de solidificar e registrar tudo o que se tem feito pela
                    arborização
                    da cidade, como patrimônio de todos.
                </p>
            </div>


