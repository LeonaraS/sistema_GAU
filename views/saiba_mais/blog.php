                <hgroup class="text-center">
                    <h1>Trabalhos acadêmicos sobre arborização urbana em Macapá</h1>
                </hgroup>


                <p>
                    Os cursos voltados para a formação academia em Ciências Ambientais e Engenharia Florestal (áreas
                    que
                    terão em seu escopo a arborização urbana) ainda são novos nas faculdades do Amapá.
                </p>

                <p>
                    Porém nos últimos anos, o interesse por estes cursos vêm crescendo e estudos investigativos e
                    TCC’s
                    sobre arborização estão disponíveis na Internet, pois além do aprendizado, estes acadêmicos
                    tiveram
                    o
                    foco de trazer apoio à Secretaria de Meio Ambiente, providenciando levantamentos até então
                    inéditos
                    já
                    que a estrutura e disponibilidade de informações são muito escassas.
                </p>

                <p>
                    Confira nesta seção alguns destes trabalhos valiosos sobre arborização e outros dados ligados ao
                    conhecimento do público sobre essa temática:
                </p>


                <p>
                    Percepção Ambiental e Arborização Urbana em Macapá, realizado nos bairros Laguinho,
                    Cidade
                    Nova,
                    Alvorada, Loteamento Vale Verde, São Lázaro e Loteamento Amazonas, de jul. a nov./12 por
                    Hugo S.
                    Castro e Teresa C. A. C. Dias. O objetivo foi avaliar o grau de conscientização dos
                    moradores
                    sobre
                    arborização urbana, mediante pesquisa com 550 pessoas. A pesquisa apontou que 54% dos
                    entrevistados
                    consideram a cidade pouco arborizada e 18% muito arborizada. Sobre as desvantagens da
                    arborização,
                    as opiniões se dividiram, mas 22% alegou a produção de sujeira nas calçadas. 60% dos
                    entrevistados
                    não sabem qual órgão é responsável por receber reclamações e sugestões sobre
                    arborização. A
                    grande
                    maioria se apresentou favorável ao plantio de novas árvores em seus bairros, porém as
                    espécies
                    preferidas são as frutíferas, inadequadas na arborização urbana (PDF disponível em:
                    <a href="../../../docs/709-3376-3-PB.pdf" target="_blanck">Acessando
                        Aqui)</a>
                </p>

                <p>
                    Entre os anos de 2015 e 2016 foram realizados estudos quali-quantitativos na Praça Marco
                    Zero
                    (PDF
                    disponível em: <a href="../../../docs/1444-2904-2-PB.pdf" target="_blanck">Acessando
                        Aqui</a>)
                    (<strong>por Maurício A. Sardinha, Francisco de Oliveira Cruz Jr e Simona Kattrynna A. da
                        Silva</strong>), na Praça Nossa Sra. da Conceição (PDF disponível em: <a
                        href="https://www.researchgate.net/profile/Nativa_Pesquisas_Agrarias_E_Ambientais/publication/304675466_Analise_quali-quantitativa_da_arborizacao_de_uma_praca_urbana_do_Norte_do_Brasil_Quali-quantitative_analysis_of_afforestation_of_a_square_urban_Brazil_North/links/5776b83308aead7ba071d104/Analise-quali-quantitativa-da-arborizacao-de-uma-praca-urbana-do-Norte-do-Brasil-Quali-quantitative-analysis-of-afforestation-of-a-square-urban-Brazil-North.pdf"
                        target="_blanck">Acessando Aqui</a>) (<strong>Ediellen M. C. Gomes, Danielle M. de S.
                        Rodrigues,
                        Jaqueline T. Santos e Elilson J. Barbosa</strong>) no bairro Trem e Praça Floriano
                    Peixoto
                    (PDF
                    disponível em: <a
                        href="https://www.researchgate.net/profile/Adelson_Rocha_Dantas/publication/311899593_DIAGNOSTICO_FLORISTICO_DA_PRACA_FLORIANO_PEIXOTO_NA_CIDADE_DE_MACAPA_AMAPA/links/585ffe8b08ae329d61fada79/DIAGNOSTICO-FLORISTICO-DA-PRACA-FLORIANO-PEIXOTO-NA-CIDADE-DE-MACAPA-AMAPA.pdf?origin=publication_detail"
                        target="_blanck">Acessando Aqui</a>) (<strong>por Adelson R. Dantas, Ediellen M. C.
                        Gomes,
                        Angélica P. Pinheiro</strong>). Foi realizado inventário das espécies, medição da copa e
                    altura,
                    a saúde das árvores, intensidade das podas, origem das espécies, entre outros.


                    <p>
                        As Geotecnologias como ferramenta p/ o diagnóstico da Arborização Urbana: O caso de
                        Macapá,
                        realizado em 2015 por Hugo S. de Castro, Teresa C. A. C. Dias e Viviane V. V. Amanajás,
                        foi
                        um
                        levantamento da arborização das vias públicas de 245 quadras aleatórias em 41 bairros,
                        utilizando o
                        software ArcGIS 8.3 (Sistema de Informação Geográfica) para obtenção de imagens e
                        catalogação
                        das
                        espécies encontradas, bem como avaliação in loco de cada árvore do inventário, de modo a
                        relatar
                        informações de porte, idade, necessidade de poda e saúde do indivíduo. Todos os dados
                        foram
                        deixados
                        à disposição da sociedade civil, acadêmica e poder público, para continuidade e tomadas
                        de
                        decisões
                        acerca da arborização da cidade. (PDF disponível em:
                        <a href="../../../docs/42281-191583-1-PB.pdf" target="_blanck">Acessando
                            Aqui
                        </a>
                    </p>

                    <p>
                        Cobertura Arbórea dos Bairros Centrais de Macapá, realizado por Iann R. Sarquis de
                        mar./14 a
                        abr./15, e teve o intuito de quantificar a cobertura arbórea de 19 bairros centrais da
                        cidade. O
                        levantamento foi realizado à distância, com uso do software ArcGIS 10 e Google Earth
                        Pro. O
                        trabalho
                        foi pioneiro, pois até então não havia documentos oficiais públicos que apontassem
                        índices
                        capazes
                        de diagnosticar se de fato esses bairros eram arborizados, bem como apontaram a
                        Densidade de
                        Árvores/Hectare e Índice de Cobertura Arbórea/Habitante. Os números espelharam a
                        realidade
                        que
                        já se
                        tem visualmente, de que a Cobertura Arbórea está bem abaixo dos padrões recomendados
                        pelos
                        estudiosos da área. O único índice que se mostrou favorável foi o de 31,70 m² por
                        habitante,
                        no
                        bairro Zerão, mas isso também é facilmente explicado, já que grande parte da área é
                        ocupada
                        pela
                        UNIFAP, que possui muitas árvores em seu entorno e poucas edificações.
                    </p>

