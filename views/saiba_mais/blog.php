                <hgroup class="text-center">
                    <h1>O que é Arborização Urbana?</h1>
                    <h2>História e conceitos</h2>
                </hgroup>

                <p>
                    Arborização Urbana pode ser definida como o conjunto da vegetação arbórea natural ou cultivada
                    que
                    uma cidade possui, representada em áreas particulares, praças, parques, vias públicas e em
                    outros
                    verdes complementares, sendo elemento fundamental no planejamento urbano, na medida em que
                    define e
                    estrutura o espaço. Em outras palavras, a árvore no quintal, na calçada, no cemitério, no
                    condomínio... todas fazem parte da arborização de uma cidade!
                </p>

                <p>
                    Segundo a História, Paris no ano de 1660 foi a primeira cidade a arborizar uma rua inteira, com
                    o
                    objetivo de ornamentá-la e proteger os movimentos militares. Mas há relatos mais antigos, como
                    os
                    Jardins Suspensos da Babilônia de 600 a.C. na Bíblia, ou também as grandes áreas de estudo e
                    debates
                    filosóficos, na Grécia antiga.
                </p>

                <p>
                    O desenvolvimento da arborização nas cidades está intimamente ligado ao Êxodo Rural, pois na
                    medida
                    em que o homem foi trocando o campo pelo meio urbano em busca de conforto, as cidades foram
                    crescendo de forma muito rápida e desordenada, sem um planejamento adequado de ocupação,
                    provocando
                    vários problemas que interferem diretamente na qualidade de vida do homem que vive na cidade. O
                    relatório da ONU de 2014 apontava que mais de 54% da população mundial vivia nas zonas urbanas.
                    No
                    Brasil, este número sobe para mais de 80% de população urbana.
                </p>

                <p>
                    Desde que as questões ambientais começaram a fazer parte da conscientização social, a vegetação,
                    como um todo, tem sido de grande importância na melhoria da qualidade de vida nos centros
                    urbanos.
                    Pode-se dizer, então, que a arborização urbana reflete um alto grau cultural da sociedade quando
                    esta entende que a vegetação, assim como o solo, o ar, e a água, são necessidades primordiais do
                    cenário urbano.
                </p>

                <p>
                    Planejar a arborização é imprescindível para o desenvolvimento urbano com o mínimo de prejuízos
                    causados ao meio ambiente, considerando que a arborização é fator determinante da saúde
                    ambiental e
                    por ter influência direta sobre o bem estar do homem, em virtude dos inúmeros benefícios que
                    proporciona a todos.
                </p>
            </div>


